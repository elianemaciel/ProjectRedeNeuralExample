class Neuronio:
    valor = 0.0
    erro = 0.0
    valorEsperado = 0
    conexoes = []

    def __init__(self, valor=0.00, erro=0, valorEsperado=0,conexoes=[]):
        self.valor = valor
        self.erro = erro
        self.valorEsperado = valorEsperado
        self.conexoes = conexoes


    def Neuronio(self):

        conexoes = []
        self.valorEsperado = 0

    def getConexoes(i):

        return conexoes.get(i)


    def somaValor(valor):

        self.valor += valor
